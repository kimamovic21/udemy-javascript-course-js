'use strict';

/*
"Classical OOP": Classes
-Objects(instances) are instantiated from a class, which functions like a blueprint
-Behavior (methods) is copied from class to all instances

OOP in Javascript: Prototypes
-Objects are linked to a prototype object
-Prototypal inheritance: The prototype contains methods (behaviour) that are accessible 
to all objects linked to that prototype
-Behavior is delegated to the linked prototype object

-Array.prototype is the prototype of all array objects we create in Javascript
-All arrays have access to the map method

3 ways of implementing prototypal inheritance in Javascript
1. Constructor functions
2. ES6 Classes
3. Object.create()
*/